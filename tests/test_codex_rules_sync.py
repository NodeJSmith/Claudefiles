def test_summary_reports_counts_and_bytes(dirs):
    rules, home = dirs
    write_rule(rules, "in1.md", "# In1\nX\n")
    write_rule(rules, "in2.md", "# In2\nY\n", tools=("claude", "codex"))
    write_rule(rules, "out.md", "# Out\nZ\n", tools=("claude",))
    result = run(rules, home)
    assert result.returncode == 0, result.stderr
    size = len((home / "AGENTS.md").read_text().encode())
    assert "2 rules included" in result.stdout
    assert "1 excluded" in result.stdout
    assert f"{size} bytes" in result.stdout


def test_unclosed_frontmatter_excluded_and_warns(dirs):
    rules, home = dirs
    write_rule(rules, "good.md", "# Good\nGOOD_SENTINEL\n")
    # Opening `---` with no closing `---`: malformed → fail-closed (Claude-only,
    # excluded from Codex), warn on stderr, and no stray `---` in the output.
    (rules / "weird.md").write_text("---\ntool: claude, codex\nWEIRD_SENTINEL\n")
    result = run(rules, home)
    assert result.returncode == 0, result.stderr
    out = (home / "AGENTS.md").read_text()
    assert "GOOD_SENTINEL" in out
    assert "WEIRD_SENTINEL" not in out
    assert "---" not in out  # no stray horizontal rule from the unclosed block
    assert "no closing" in result.stderr.lower()


def test_list_mode_reports_without_writing(dirs):
    rules, home = dirs
    write_rule(rules, "portable.md", "# P\nBODY\n")
    write_rule(rules, "claude-only.md", "# C\nBODY\n", tools=("claude",))
    result = run(rules, home, "--list")
    assert result.returncode == 0, result.stderr
    assert "include  portable.md" in result.stdout
    assert "exclude  claude-only.md" in result.stdout
    assert not (home / "AGENTS.md").exists()  # --list never writes


def test_list_mode_works_when_nothing_tagged_for_codex(dirs):
    # --list is a diagnostic: it must succeed (not abort) even when no rule is
    # tagged for Codex — that's exactly when you'd run it to find out why.
    rules, home = dirs
    write_rule(rules, "a.md", "# A\nBODY\n", tools=("claude",))
    write_rule(rules, "b.md", "# B\nBODY\n", tools=None)
    result = run(rules, home, "--list")
    assert result.returncode == 0, result.stderr
    assert "0 included" in result.stdout
    assert "exclude  a.md" in result.stdout


@pytest.mark.skipif(
    "/.claude/worktrees/" not in str(SCRIPT),
    reason="worktree guard only triggers from a worktree checkout",
)
def test_worktree_guard_refuses_default_rules(tmp_path):
    # No --rules-dir (so it uses the in-worktree default) and no --allow-worktree:
    # the generator must refuse rather than clobber the machine-global AGENTS.md.
    home = tmp_path / "codex"
    home.mkdir()
    result = subprocess.run(
        [sys.executable, str(SCRIPT)],
        capture_output=True,
        text=True,
        env={**os.environ, "CODEX_HOME": str(home)},
    )
    assert result.returncode != 0
    assert "worktree" in result.stderr.lower()
    assert not (home / "AGENTS.md").exists()


@pytest.mark.skipif(shutil.which("codex") is None, reason="codex binary not installed")
def test_codex_loads_global_file_head_and_tail(dirs):
    rules, home = dirs
    # Distinct head and tail sentinels (sorted filenames put aaa first, zzz last).
    write_rule(rules, "aaa.md", "# AAA\nHEAD_SENTINEL_8f3a\n")
    write_rule(rules, "zzz.md", "# ZZZ\nTAIL_SENTINEL_2c91\n")
    assert run(rules, home).returncode == 0

    proc = subprocess.run(
        ["codex", "debug", "prompt-input"],
        capture_output=True,
        text=True,
        cwd=str(home.parent),
        env={**os.environ, "CODEX_HOME": str(home)},
    )
    combined = proc.stdout + proc.stderr
    assert "HEAD_SENTINEL_8f3a" in combined
    assert "TAIL_SENTINEL_2c91" in combined
