"""Tests for bin/codex-rules-sync.

Each test builds fixture rule files in a temp dir, runs the generator via subprocess
with --rules-dir and a temp $CODEX_HOME, and asserts on the written AGENTS.md, the
summary on stdout, and the exit code. Subprocess (not import) matches the repo's
script-testing convention (tests/test_hooks.py) and exercises the real CLI surface.

Classification is a `tool:` frontmatter allowlist, fail-closed: a rule reaches Codex
only when its `tool:` list contains `codex`; absent/other → Claude-only → excluded.
"""

import os
import shutil
import subprocess
import sys
import tempfile
from pathlib import Path

import pytest

ROOT = Path(__file__).resolve().parent.parent
SCRIPT = ROOT / "bin" / "codex-rules-sync"

GENERATED_FIRST_LINE = (
    "<!-- GENERATED BY codex-rules-sync — DO NOT EDIT MANUALLY. "
    "Run codex-rules-sync to regenerate. -->"
)


def write_rule(
    rules_dir: Path,
    name: str,
    body: str,
    tools: tuple[str, ...] | None = ("claude", "codex"),
) -> None:
    """Write a fixture rule. tools=None → no frontmatter (fail-closed: Codex-excluded)."""
    fm = ""
    if tools is not None:
        fm = f"---\ntool: {', '.join(tools)}\n---\n\n"
    (rules_dir / name).write_text(fm + body)


def run(rules_dir: Path, codex_home: Path, *extra: str) -> subprocess.CompletedProcess:
    return subprocess.run(
        [sys.executable, str(SCRIPT), "--rules-dir", str(rules_dir), *extra],
        capture_output=True,
        text=True,
        env={**os.environ, "CODEX_HOME": str(codex_home)},
    )


@pytest.fixture
def dirs(tmp_path: Path) -> tuple[Path, Path]:
    rules = tmp_path / "rules"
    rules.mkdir()
    home = tmp_path / "codex"
    home.mkdir()
    return rules, home


def test_tool_allowlist_includes_codex_excludes_others(dirs):
    rules, home = dirs
    write_rule(
        rules,
        "portable.md",
        "# Portable\nPORTABLE_SENTINEL\n",
        tools=("claude", "codex"),
    )
    write_rule(
        rules, "claude-only.md", "# Claude Only\nCLAUDE_SENTINEL\n", tools=("claude",)
    )
    write_rule(rules, "untagged.md", "# Untagged\nUNTAGGED_SENTINEL\n", tools=None)

    result = run(rules, home)
    assert result.returncode == 0, result.stderr
    out = (home / "AGENTS.md").read_text()
    assert "PORTABLE_SENTINEL" in out
    assert "CLAUDE_SENTINEL" not in out  # tool: claude → excluded
    assert "UNTAGGED_SENTINEL" not in out  # fail-closed: no tool: key → excluded


def test_tool_mention_in_body_does_not_classify(dirs):
    rules, home = dirs
    # A `tool: codex` mention in the body must not opt a Claude-only rule into Codex —
    # only the frontmatter key counts.
    write_rule(rules, "portable.md", "# P\nPORTABLE_SENTINEL\n")
    write_rule(
        rules,
        "claude-only.md",
        "# Claude Only\nThis rule is for `tool: codex` agents too, someday.\nBODY_SENTINEL\n",
        tools=("claude",),
    )
    result = run(rules, home)
    assert result.returncode == 0, result.stderr
    out = (home / "AGENTS.md").read_text()
    assert "PORTABLE_SENTINEL" in out
    assert "BODY_SENTINEL" not in out


