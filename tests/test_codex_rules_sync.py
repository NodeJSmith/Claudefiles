def test_header_precedence_note_and_source_markers(dirs):
    rules, home = dirs
    write_rule(rules, "alpha.md", "# Alpha\nALPHA_BODY\n")
    result = run(rules, home)
    assert result.returncode == 0, result.stderr
    out = (home / "AGENTS.md").read_text()
    lines = out.splitlines()
    assert lines[0] == GENERATED_FIRST_LINE
    assert "may add to or override" in out  # precedence note
    # rules-dir is a temp dir outside the repo, so the marker uses the fallback
    # `<dir-name>/<file>` form (see test_repo_relative_marker for the in-repo path).
    assert "<!-- from rules/alpha.md -->" in out
    marker_idx = next(i for i, ln in enumerate(lines) if ln.endswith("alpha.md -->"))
    assert lines[marker_idx + 1] == "# Alpha"


def test_repo_relative_marker_for_in_repo_rules():
    # Run over the real rules/common so the source marker exercises the
    # relative_to(ROOT) success path, not just the fallback. --allow-worktree because
    # the default rules-dir lives in this worktree during development.
    rules = ROOT / "rules" / "common"
    with tempfile.TemporaryDirectory() as td:
        result = subprocess.run(
            [
                sys.executable,
                str(SCRIPT),
                "--rules-dir",
                str(rules),
                "--allow-worktree",
            ],
            capture_output=True,
            text=True,
            env={**os.environ, "CODEX_HOME": td},
        )
        assert result.returncode == 0, result.stderr
        out = (Path(td) / "AGENTS.md").read_text()
    assert "<!-- from rules/common/coding-style.md -->" in out


def test_deterministic(dirs):
    rules, home = dirs
    write_rule(rules, "a.md", "# A\nBODY_A\n")
    write_rule(rules, "b.md", "# B\nBODY_B\n", tools=("claude",))
    write_rule(rules, "c.md", "# C\nBODY_C\n", tools=("claude", "codex"))

    assert run(rules, home).returncode == 0
    first = (home / "AGENTS.md").read_text()
    assert run(rules, home).returncode == 0
    second = (home / "AGENTS.md").read_text()
    assert first == second


def test_missing_codex_home_skips_without_writing(dirs):
    rules, _ = dirs
    write_rule(rules, "a.md", "# A\nBODY\n")
