def test_empty_skill_set_refuses_to_remove_generated_wrappers(tmp_path: Path):
    repo = tmp_path / "repo"
    skills = tmp_path / "installed-skills"
    skills.mkdir()
    commands = tmp_path / "commands"
    commands.mkdir()
    generated = commands / "mine-debug.md"
    generated.write_text(
        "<!-- GENERATED BY opencode-skills-commands-sync — DO NOT EDIT MANUALLY. -->\n"
    )

    result = run(repo, commands, "--skills-dir", str(skills))

    assert result.returncode != 0
    assert "no user-invocable skills found" in result.stderr
    assert generated.exists()
